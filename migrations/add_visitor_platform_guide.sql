  '# Welcome to Dzaleka Visit!

Our platform is designed to make your visit to Dzaleka Refugee Camp seamless, educational, and impactful. Here is a guide to all the features available to you as a visitor.

## 1. Dashboard
Your central hub for everything.
- **Upcoming Visits:** See your next scheduled tour at a glance.
- **Quick Stats:** Track your total bookings and completed tours.
- **Quick Actions:** Easily book a new visit or access the Learning Center.

## 2. Booking a Tour
We offer several tour types to suit your interests:
- **Standard Tour:** A comprehensive introduction to Dzaleka.
- **Extended Tour:** A deeper dive into specific areas or community projects.
- **Custom Tour:** Tailored to your specific professional or personal interests.

To book:
1. Navigate to "Book a Visit" or "My Bookings".
2. Select your date, time, and group size.
3. Choose your tour type.
4. Confirm your booking.

## 3. Managing Your Bookings
- **View Details:** Click on any booking to see the meeting point, assigned guide, and itinerary.
- **Reschedule/Cancel:** Plans change! You can request a change or cancellation directly from the booking details page.
- **Download Receipt:** Get a record of your booking for expenses.

## 4. Payments
We support convenient local and cash payment methods:
- **Mobile Money:** Pay securely via Airtel Money or TNM Mpamba.
- **Cash:** Pay upon arrival at the camp (Subject to verification).

## 5. Learning Center
Before you arrive, explore our rich library of resources:
- **Cultural Guides:** Learn about the diverse cultures within Dzaleka.
- **Do''s and Don''ts:** Essential etiquette for a respectful visit.
- **History:** Understand the background of the camp.
- **Track Progress:** Mark modules as read as you prepare for your trip.

## 6. Support & Safety
- **Help Center:** Browse FAQs (like this one!) for instant answers.
- **Support Tickets:** Have a specific issue? Submit a ticket to our admin team.
- **Emergency Contacts:** Access key security and support numbers directly from your dashboard.

## 7. Your Profile
Keep your contact information and preferences up to date to ensure our guides can provide the best experience for you.

We hope you have a meaningful and transformative visit!',
