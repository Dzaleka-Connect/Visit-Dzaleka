export function generateVisitorReport(data: { total: number; checkedIn: number; period: string }) {
  const checkInRate = data.total > 0 ? Math.round((data.checkedIn / data.total) * 100) : 0;
  const content = `
    <table role="presentation" width="100%" cellpadding="0" cellspacing="0">
      <tr>
        ${metricCard("New bookings", formatNumber(data.total), "#2563eb", "Bookings created during this period.")}
        ${metricCard("Checked in", formatNumber(data.checkedIn), "#0f766e", `${checkInRate}% check-in rate for the period.`)}
      </tr>
    </table>
    ${insightPanel(
      "Operational note",
      data.total === 0
        ? "No new visitor bookings were created during this reporting window."
        : "Review upcoming bookings, guide assignment, transport needs, and visitor requirements before the next tour day.",
      "#2563eb"
    )}
  `;
  return emailWrapper("Visitor Summary", "Visitor Operations", data.period, content);
}

export function generateRevenueReport(data: { total: number; pending: number; period: string }) {
  const totalPipeline = data.total + data.pending;
  const collectedRate = totalPipeline > 0 ? Math.round((data.total / totalPipeline) * 100) : 0;
  const content = `
    <table role="presentation" width="100%" cellpadding="0" cellspacing="0">
      <tr>
        ${metricCard("Collected", formatMwk(data.total), "#059669", `${collectedRate}% of active payment value is paid.`)}
        ${metricCard("Pending", formatMwk(data.pending), "#d97706", "Open payment value for non-cancelled bookings.")}
      </tr>
    </table>
    ${insightPanel(
      "Finance note",
      data.pending > 0
        ? "Follow up pending payment bookings and reconcile recent card, cash, and mobile money records."
        : "No pending payment value was recorded for this reporting window.",
      "#d97706"
    )}
  `;
  return emailWrapper("Revenue Summary", "Finance", data.period, content);
}

export function generateIncidentReport(data: { newIncidents: number; unresolved: number; period: string }) {
  const hasRisk = data.newIncidents > 0 || data.unresolved > 0;
  const content = `
    <table role="presentation" width="100%" cellpadding="0" cellspacing="0">
      <tr>
        ${metricCard("New incidents", formatNumber(data.newIncidents), hasRisk ? "#dc2626" : "#0f766e", "Incidents reported during this period.")}
        ${metricCard("Unresolved", formatNumber(data.unresolved), data.unresolved > 0 ? "#dc2626" : "#0f766e", "Incidents still open or under investigation.")}
      </tr>
    </table>
    ${insightPanel(
      hasRisk ? "Security follow-up" : "Security status",
      hasRisk
        ? "Review open incidents, assign owners, and record resolution notes before closing cases."
        : "No open security follow-up was detected at the time this report was generated.",
      hasRisk ? "#dc2626" : "#0f766e"
    )}
  `;
  return emailWrapper("Security Incident Summary", "Security", data.period, content);
}
