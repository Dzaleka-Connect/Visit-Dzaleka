const GENERATED_AT = () =>
  new Intl.DateTimeFormat("en-MW", {
    dateStyle: "medium",
    timeStyle: "short",
    timeZone: "Africa/Blantyre",
  }).format(new Date());

const formatNumber = (value: number) =>
  new Intl.NumberFormat("en-MW", { maximumFractionDigits: 0 }).format(value || 0);

const formatMwk = (amount: number) =>
  new Intl.NumberFormat("en-MW", {
    style: "currency",
    currency: "MWK",
    maximumFractionDigits: 0,
  }).format(amount || 0);

